Cycle length varies widely between individuals and even from month to month. A cycle anywhere from **21 to 35 days** is generally considered normal. Ovulation typically occurs **14 days before your next period**, not necessarily on day 14 of your cycle — this distinction matters enormously if your cycle is not 28 days.

