### 1. Menstruation (Days 1–5 on average)
Your cycle officially starts on the first day of your period. The uterine lining sheds when pregnancy has not occurred. Hormone levels — estrogen and progesterone — are at their lowest.

### 2. Follicular Phase (Days 1–13 on average)
Overlapping with menstruation, this phase begins as follicle-stimulating hormone (FSH) triggers follicles in your ovaries to mature. One dominant follicle emerges and produces estrogen, which thickens the uterine lining in preparation for a potential embryo.

