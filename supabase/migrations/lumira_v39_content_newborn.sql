Most newborns feed **8 to 12 times in 24 hours**. This may feel relentless, but each feed is a signal to your body to make more milk. Watching your baby for hunger cues -- rooting, sucking on hands, turning the head side to side -- rather than watching the clock leads to better outcomes.

