A good latch is the single most important factor in comfortable breastfeeding. A shallow latch causes **nipple pain**, reduces how much milk your baby transfers, and can signal to your body to make less milk. A deep latch means your baby does the work with their jaw and tongue -- not their gums on your nipple tip.

