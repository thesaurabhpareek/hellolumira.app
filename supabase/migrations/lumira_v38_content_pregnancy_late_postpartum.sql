'## How Babies Are Positioned in the Womb

By around 32–36 weeks, most babies will have settled into their final position ready for birth. Understanding where your baby is lying can help you prepare — and know what to expect at your antenatal appointments.

## The Ideal Position: Cephalic (Head Down)

The most common and preferred position for birth is **cephalic presentation**, where your baby''s head is pointing down toward your cervix. Within this, the **occiput anterior (OA)** position — where the back of baby''s head faces your abdomen — is ideal for labour, as it allows the head to navigate the pelvis most efficiently.

**Left occiput anterior (LOA)** is the most favourable position, though right occiput anterior is also fine. You might feel kicks under your ribs and a firm, rounded bump near your belly button — the baby''s bottom.

## Occiput Posterior (Back-to-Back)

If your baby is head down but facing your abdomen rather than your spine, this is called **occiput posterior (OP)** or "back-to-back." Labour can be longer and more uncomfortable, particularly in your lower back. Many babies rotate during labour, but some are born in this position.

**Things that may help encourage rotation:**
- Spending time on all fours
- Leaning forward over a birth ball
- Staying upright and active

## Breech Presentation

Around **3–4% of babies** remain in a breech position at full term — bottom or feet pointing down. Your midwife will usually identify this at your 36-week appointment.

**Types of breech:**
- **Frank breech** — bottom down, legs straight up
- **Complete breech** — bottom down, knees bent
- **Footling breech** — one or both feet presenting first

### What happens if my baby is breech?

Your care team will discuss your options:

- **ECV (External Cephalic Version):** A procedure (offered from 36 weeks) where a doctor tries to manually turn the baby from outside your abdomen. Success rates are around 50%.
- **Planned C-section:** Often recommended for breech babies.
- **Vaginal breech birth:** Possible in some units with experienced practitioners.

## Transverse Lie

A small number of babies lie sideways across the uterus. This is called a **transverse lie** and is not safe for vaginal birth. If your baby remains in this position near your due date, a C-section will be planned.

## How Your Midwife Checks Position

At each appointment from around 28 weeks, your midwife will use **Leopold''s manoeuvres** — a series of gentle abdominal palpations — to feel where baby''s head, back, and bottom are. An ultrasound scan can confirm position if there is any uncertainty.

## Can I Encourage My Baby to Turn?

Some parents find the following helpful for encouraging an optimal position, though evidence is limited:

- **Optimal Foetal Positioning (OFP):** Leaning forward regularly, avoiding reclining for long periods
- **Moxibustion:** A traditional Chinese medicine technique; discuss with your midwife before trying
- **Swimming:** The buoyancy can encourage baby to shift

## When to Talk to Your Care Team

If your baby is in a non-cephalic position at 36 weeks or later, your midwife or obstetrician will discuss your options in detail. Every situation is different, and the right choice depends on your baby''s specific position, your health, and your birth preferences.',
