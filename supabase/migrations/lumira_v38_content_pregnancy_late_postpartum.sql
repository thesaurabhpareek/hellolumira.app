'## Pain in Labour: What to Expect

Labour pain is real, intense, and completely individual. Some people find it manageable with breathing and movement; others find epidural anaesthesia a life-changing relief. There is no right or wrong approach — only what works for you.

Understanding your options in advance helps you make informed decisions in the moment, even if your preferences change once labour is underway.

## Non-Pharmacological Options

### Breathing and Relaxation Techniques
Focused breathing — breathing slowly through contractions — activates the parasympathetic nervous system and reduces the perception of pain. Hypnobirthing, yoga, and mindfulness all draw on this.

### TENS Machine (Transcutaneous Electrical Nerve Stimulation)
Pads placed on your back deliver small electrical pulses that interfere with pain signals and stimulate endorphin release. Works best in **early labour**. Safe, drug-free, and you control the intensity. Hire or buy one from about 36 weeks.

### Water / Hydrotherapy
Warm water — whether a pool, birth pool, or shower — is one of the most effective natural pain relievers. It eases muscle tension and can slow a very fast labour. Available in many midwifery-led units and suitable for home births.

### Movement and Position
Staying upright and mobile opens the pelvis and uses gravity. Rocking, walking, swaying on a birth ball, and position changes all help manage pain and support labour progress.

### Massage and Counter-Pressure
Your birth partner applying firm pressure to your lower back during contractions can significantly reduce pain perception, especially in back labour.

## Pharmacological Options

### Gas and Air (Entonox)
A 50/50 mixture of nitrous oxide and oxygen, inhaled through a mouthpiece at the start of each contraction. It doesn''t remove pain but takes the edge off and gives you something to focus on. Effects wear off within seconds of stopping. Available in almost all UK birth settings.

**Side effects:** Dizziness, nausea, light-headedness. Some people find it makes them feel disconnected.

### Opioid Injections (Pethidine / Diamorphine / Meptid)
Injected into your thigh or buttock, opioids take the edge off contractions and may help you rest in early labour. They don''t fully eliminate pain.

**Side effects:** Drowsiness, nausea, can slow baby''s breathing if given close to birth (reversible with an injection of naloxone). Not available if birth is imminent.

### Epidural Anaesthesia
A fine catheter is placed in the epidural space of your lower back by an anaesthetist, delivering a continuous infusion of local anaesthetic. A **walking epidural** uses a lower dose that allows some sensation and movement.

**Benefits:** Highly effective pain relief; allows you to rest; can be topped up for C-section.

**Considerations:** Slows labour in some cases, requiring a syntocinon drip; reduces sensation for pushing; carries small risk of severe headache (dural tap); continuous monitoring required. Takes 10–20 minutes to work.

## Spinal Block

Used mainly for planned C-sections or when an epidural isn''t in place. A single injection into the spinal fluid provides rapid, complete numbness from the waist down.

