## Sleep Positions in Late Pregnancy

From around **28 weeks**, you are advised to go to sleep on your **left or right side** rather than on your back. Lying flat on your back for extended periods can compress the inferior vena cava (a major blood vessel), reducing blood flow to the baby and potentially causing dizziness or faintness.

**The BUMP study (Tommy''s)** found that going to sleep on your back in late pregnancy was associated with a higher risk of stillbirth. Going back to sleep on your side if you wake on your back is what matters — your body will naturally adjust.

### The pillow strategy:
- **A pregnancy pillow** (full-length, U-shape, or wedge) can transform comfort
- Place one pillow between your knees to align hips
- Use a wedge pillow under the bump
- Support your upper back with a pillow behind you

## Practical Strategies for Better Sleep

- **Elevate the head of the bed** (or use extra pillows) to reduce heartburn
- **Eat dinner earlier** and avoid large meals close to bedtime
- **Limit fluids after 7pm** to reduce night-time trips, while staying hydrated during the day
- **Gentle exercise during the day** (not too close to bedtime) improves sleep quality
- **Create a bedtime routine** — wind down with a warm bath, reading, or relaxation
- **Keep the bedroom cool and dark**
- **Limit screen time** an hour before bed
- **Nap if you can** — guilt-free daytime naps are encouraged in the third trimester

## When to Ask for Help

- If **restless leg syndrome** is severe, discuss it with your midwife — iron or folate supplementation sometimes helps
- If **anxiety** is significantly disrupting your sleep, speak to your GP or midwife about psychological support
- If you snore loudly or wake gasping, mention it — **pregnancy sleep apnoea** is more common than recognised and can be assessed

