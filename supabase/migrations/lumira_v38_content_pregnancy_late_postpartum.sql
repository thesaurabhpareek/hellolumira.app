'## What Is a Caesarean Section?

A caesarean section (C-section) is a surgical procedure in which your baby is born through an incision in your abdomen and uterus. In the UK, around **30–35%** of births are by C-section — either planned (elective/scheduled) or unplanned (emergency).

A C-section is a major abdominal operation, but it is a normal way to give birth. With good support, it can be a positive experience.

## Types of C-Section

### Planned (Elective) C-Section
Scheduled in advance, usually from 39 weeks. Reasons include:
- Breech or transverse baby at term
- Placenta praevia (low-lying placenta)
- Previous C-section with certain complications
- Medical conditions making vaginal birth inadvisable
- Maternal request (discuss with your obstetrician)

### Unplanned (Emergency) C-Section
Decided during labour when:
- Labour is not progressing
- Baby shows signs of distress
- Complications arise requiring urgent delivery

Emergency does not always mean imminent danger — categories range from "immediate threat to life" to "no maternal or fetal compromise but needs expedited delivery."

## What Happens During a C-Section

### Anaesthesia
Most C-sections use a **spinal block** (or epidural top-up if already in place), which numbs you from the waist down. You are awake and aware. General anaesthesia is reserved for true emergencies.

### In the Operating Theatre
- A screen is placed across your chest, though many units offer a **lowered screen or clear drape** for a "gentle" or "natural" caesarean so you can see your baby being born
- Your partner or support person can usually be present
- The procedure from incision to birth takes around **5–10 minutes**
- Closing the incision takes a further 30–45 minutes

### What You''ll Feel
No pain, but you will feel **pressure and pulling** as the baby is manoeuvred out. Many people find this sensation surprising but not distressing.

## The Natural Caesarean

