Every experience is different:

- **A sudden gush** — like a balloon popping, often accompanied by a large amount of warm fluid
- **A slow trickle** — an ongoing leak that you might mistake for urine or increased vaginal discharge
- **A pop sensation** followed by fluid

Amniotic fluid is typically **clear or slightly straw-coloured** and odourless. Unlike urine, you cannot stop the flow by tightening your pelvic floor.

