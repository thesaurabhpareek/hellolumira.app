'## What Is Group B Streptococcus?

Group B Streptococcus (GBS) is a type of bacteria that lives naturally in the gut and vagina of around **20–30% of adults**. In healthy adults it causes no symptoms and is completely harmless — but it can pose a risk to newborns if passed during labour and birth.

## Why Does It Matter in Pregnancy?

A baby can pick up GBS bacteria while passing through the birth canal. Most babies who come into contact with GBS will be fine, but in a small number of cases it can cause **early-onset GBS infection** — a serious illness that can lead to sepsis, pneumonia, or meningitis in the newborn.

The risk of a baby developing GBS infection without any preventative treatment is roughly **1 in 400** among those who carry GBS. With **intravenous antibiotics in labour**, this risk drops significantly.

## How Is It Tested?

GBS testing is not universally offered on the NHS as a routine screen, though this is changing in some regions. Testing is available:

- **Via a private swab test** — a low vaginal and rectal swab taken at 35–37 weeks (the optimal window)
- **Through your NHS team** if you have risk factors

If you want to be tested, speak to your midwife. The **Group B Strep Support charity (GBSS)** also offers test kits.

## Risk Factors That Trigger Antibiotic Treatment

Even without a swab result, your team may recommend IV antibiotics in labour if you have any of the following:

- A previous baby affected by GBS infection
- GBS detected in urine during this pregnancy
- Labour before 37 weeks
- Prolonged rupture of membranes (waters broken for over 18 hours)
- Fever in labour

## What If My Result Is Positive?

A positive GBS swab does **not** mean you or your baby will be harmed. It simply means you will be offered **IV antibiotics during labour** (typically penicillin every 4 hours). This is highly effective at reducing the risk of passing GBS to your baby.

**It does not mean:**
- You need a C-section
- You can''t have a water birth (discuss options with your team)
- Your baby will definitely be infected

## After Birth

If you received antibiotics in labour at least 4 hours before birth, your baby will generally be observed for a minimum period but may not need any further treatment. If antibiotics were not given or were given late, your baby will be monitored more closely for signs of infection.

## Signs of GBS Infection in a Newborn

Seek urgent medical attention if your baby shows:

- Abnormal breathing or grunting
- Floppiness or poor feeding
- High or very low temperature
- Pale or mottled skin
- Irritability or unusual crying

## Questions to Ask Your Midwife

- Can I be tested for GBS on the NHS given my history?
- What are your unit''s protocols around GBS-positive mothers?
- Will testing affect my birth plan?

GBS is manageable with the right preparation. Talk to your care team early so you can make informed choices.',
