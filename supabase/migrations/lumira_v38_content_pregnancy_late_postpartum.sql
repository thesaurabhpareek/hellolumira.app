- Loneliness amplifies anxiety — worried thoughts spiral when there is no one to reality-check with
- Social isolation increases depression risk significantly
- Without comparison to other parents, you may assume everyone else is coping better than you
- The absence of shared experience makes the normal challenges of early parenthood feel catastrophic
- Practical help doesn''t materialise unless people know you need it

