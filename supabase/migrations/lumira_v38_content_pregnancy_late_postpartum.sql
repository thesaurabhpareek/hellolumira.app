'## Labour in Three Stages

Vaginal birth unfolds in three distinct stages. Understanding what your body is doing at each point can help you work with — rather than against — the process.

## First Stage: Dilation

The first stage is the longest and has two phases.

### Early (Latent) Labour — 0 to 6 cm dilation
Your cervix gradually softens, shortens (effaces), and begins to dilate. Contractions are irregular at first, becoming more regular — typically every 5–10 minutes, lasting 30–60 seconds.

This phase can last **hours to days**, especially for first-time parents. You are encouraged to stay home, rest, eat lightly, and stay hydrated. Call your midwife when contractions are regular and strong.

### Active Labour — 6 to 10 cm dilation
Contractions are now strong, regular, and typically 3–5 minutes apart, lasting 60–90 seconds. This is the phase most people associate with labour. Progress is usually about **1 cm per hour** once active labour is established, though wide variation is normal.

**What helps:**
- Staying upright and moving
- Breathing through each contraction
- Using your chosen pain relief
- Your birth partner providing support and advocacy

