'## What Is Induction of Labour?

Induction of labour (IOL) is the medical process of starting contractions artificially when labour hasn''t begun on its own. In the UK, around **1 in 3 labours** are induced. It''s one of the most common obstetric interventions — and understanding it can help you feel far less anxious if it''s offered to you.

## Common Reasons for Induction

- **Post-dates pregnancy** — most units offer induction at **41–42 weeks** due to rising risk of stillbirth beyond this point
- **Preeclampsia or high blood pressure**
- **Gestational diabetes**
- **Reduced fetal movement** or concerns about baby''s growth
- **Waters breaking without labour starting** (after 24 hours)
- **Medical conditions** affecting mother or baby

Induction is always a recommendation — you have the right to ask questions and make an informed decision about whether to accept it.

## Methods of Induction

Your care team will assess your cervix''s readiness (using the **Bishop score**) and recommend the most appropriate method.

### 1. Membrane Sweep (not a full induction)
A midwife inserts a finger through the cervix to separate the membranes from the uterine wall, releasing prostaglandins. It can encourage labour to start naturally, avoiding formal induction. It may be uncomfortable but is quick.

### 2. Prostaglandin (Gel or Pessary)
A gel or slow-release pessary containing prostaglandins is placed near the cervix to soften (ripen) it. This can take **6–24 hours** and may need to be repeated. You''ll be monitored and can usually move around.

### 3. Breaking the Waters (Amniotomy / ARM)
A midwife uses a small hook to make a controlled rupture of the amniotic sac. This is only done once the cervix is sufficiently dilated. Contractions usually follow within a few hours.

### 4. Syntocinon (Oxytocin) Drip
A synthetic hormone drip is administered through a cannula to stimulate contractions. It is carefully titrated up to achieve regular contractions. This method usually means **continuous fetal monitoring** throughout.

## What to Expect

- Induction can take **hours to days** — particularly for a first baby with an unripe cervix
- You will be on the labour ward or an antenatal ward for part of the process
- Contractions from induced labour can sometimes feel stronger and more intense than spontaneous labour
- Your pain relief options remain the same

## Risks and Benefits

**Benefits:** Reduces risk of stillbirth in post-dates pregnancies; addresses specific clinical concerns.

**Risks:** Increased likelihood of further interventions (continuous monitoring, oxytocin drip); contractions may be more intense; small increase in C-section risk compared to spontaneous labour in some groups.

## Your Rights

You can ask:
- **Why** induction is being recommended
- **What are the risks of waiting?**
- **What happens if I decline?**
- **Can we wait and monitor instead?** (watchful waiting)

