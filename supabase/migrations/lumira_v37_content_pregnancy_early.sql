 '## Why Genetic Screening Is Offered

Genetic screening tests look for chromosomal conditions such as Down syndrome (trisomy 21), Edwards syndrome (trisomy 18), and Patau syndrome (trisomy 13), as well as certain genetic disorders. These tests are offered to all pregnant people, not just those over 35. The decision to test — and what to do with results — is entirely personal.

## Screening Tests vs. Diagnostic Tests

**Screening tests** estimate the probability of a condition. A "positive" or "high-risk" result means further testing is recommended, not that a condition is definitely present. **Diagnostic tests** give definitive answers but carry small risks.

## Non-Invasive Prenatal Testing (NIPT)

NIPT analyses cell-free fetal DNA circulating in the mother''s blood. It is performed from week 10 onwards and screens for the most common chromosomal conditions with high accuracy.

- **Pros:** Non-invasive (blood draw only), very low false-positive rate, can also reveal fetal sex
- **Cons:** Screening test only — not diagnostic; rarer conditions may not be detected; results can rarely reflect maternal chromosomal variation
- **Cost:** May not be covered by public health systems; widely available privately

## Combined First Trimester Screening

Offered between weeks 11 and 14, this combines:
- Nuchal translucency ultrasound (measuring fluid at the back of the baby''s neck)
- Blood tests for pregnancy-associated plasma protein A (PAPP-A) and hCG

Results are combined with maternal age to produce a risk estimate. It screens for Down syndrome and some other conditions.

