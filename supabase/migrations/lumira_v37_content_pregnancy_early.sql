Morning sickness — nausea and vomiting of pregnancy (NVP) — affects up to 80% of pregnant people. Despite its name, it is not confined to the morning. For most, it peaks between weeks 8 and 10 and resolves by week 14–16. A smaller number experience symptoms throughout pregnancy.

